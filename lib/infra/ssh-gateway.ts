/** Look up the dev container name for a user's project (for the map file). */
function devContainerForProject(projectId: string): {
  slug: string;
  container: string;
} | null {
  const p = read().projects.find((x) => x.id === projectId);
  if (!p) return null;
  return { slug: p.slug, container: `deplo-dev-${p.slug}` };
}

/** Run a command inside the gateway container. */
function gwExec(args: string[], opts: { input?: string } = {}) {
  return docker(["exec", "-i", GATEWAY_CONTAINER, ...args], {
    timeout: 30_000,
    input: opts.input,
    noThrow: true,
  });
}

/**
 * Provision one user inside the running gateway (no sshd reload — sshd reads
 * accounts/keys/maps per-connection). Creates the locked-down account, sets the
 * credential(s), writes authorized_keys (key users) and the root-owned map file.
 * Decrypts the password just-in-time; it is never passed via env or argv that
 * `docker inspect` could surface.
 */
export async function provisionUser(user: DevSshUser): Promise<void> {
  const target = devContainerForProject(user.projectId);
  if (!target) return;

  // 1. Account: shell is the wrapper; member of the dev group. Idempotent.
  await gwExec([
    "sh",
    "-c",
    `id ${shellQuote(user.username)} >/dev/null 2>&1 || ` +
      `adduser -D -G ${DEV_GROUP} -s ${WRAPPER_PATH} ${shellQuote(user.username)}`,
  ]);

  // 2. Credentials. For a key-only user, DISABLE password login (shadow = "*")
  // but do NOT *lock* the account: with `UsePAM no`, a locked account ("!...")
  // is refused for EVERY auth method — including the public key the user relies
  // on ("account is locked"). "*" forbids password auth while leaving pubkey
  // auth working, which is exactly what a key-only user needs.
  if (user.passwordEnc) {
    const pw = decryptSecret(user.passwordEnc);
    // Pass cleartext over STDIN, never argv/env (inspect-safe). chpasswd reads
    // "user:password" from stdin.
    await gwExec(["sh", "-c", "chpasswd"], {
      input: `${user.username}:${pw}\n`,
    });
  } else {
    // usermod -p '*' sets the shadow hash to a value no password can match,
    // without setting the lock flag. (passwd -l would prepend '!' → locked.)
    await gwExec(["usermod", "-p", "*", user.username]);
  }

  // 3. authorized_keys (key users only). sshd reads this AS the connecting user
  // (after dropping privileges), so a root:root 0600 file is unreadable by them
  // ("Could not open authorized keys: Permission denied"). It must be
  // world-READABLE; StrictModes still passes because it is root-OWNED and not
  // group/other-WRITABLE (0644). The key itself is public, so 0644 leaks
  // nothing. `restrict` keeps the session locked down regardless.
  const keyDir = `/data/ssh-gateway/keys/${user.username}`;
  if (user.publicKey && user.publicKey.trim()) {
    const line = `restrict,pty ${user.publicKey.trim()}\n`;
    await gwExec(["sh", "-c", `mkdir -p ${shellQuote(keyDir)} && chmod 755 ${shellQuote(keyDir)}`]);
    await gwExec(
      ["sh", "-c", `cat > ${shellQuote(`${keyDir}/authorized_keys`)} && chmod 644 ${shellQuote(`${keyDir}/authorized_keys`)}`],
      { input: line },
    );
  } else {
    await gwExec(["sh", "-c", `rm -rf ${shellQuote(keyDir)}`]);
  }

  // 4. Map file (atomic tmp+rename). SLUG + DEV_CONTAINER only — NOT secret (the
  // container name is derivable from the slug). It must be world-READABLE (0644)
  // because the ForceCommand wrapper sources it while running AS the SSH user,
  // not root — sshd drops to the user's UID before ForceCommand. It stays
  // root-OWNED in a root-owned dir so the user can read but not tamper with
  // their own exec target.
  const mapBody =
    `SLUG=${shellQuote(target.slug)}\n` +
    `DEV_CONTAINER=${shellQuote(target.container)}\n`;
  const mapPath = `/data/ssh-gateway/map/${user.username}`;
  await gwExec(
    [
      "sh",
      "-c",
      `umask 022 && cat > ${shellQuote(`${mapPath}.tmp`)} && ` +
        `chown root:root ${shellQuote(`${mapPath}.tmp`)} && ` +
        `chmod 644 ${shellQuote(`${mapPath}.tmp`)} && ` +
        `mv ${shellQuote(`${mapPath}.tmp`)} ${shellQuote(mapPath)}`,
    ],
    { input: mapBody },
  );
}

/** Remove one user from the gateway (account + key + map files). */
export async function deprovisionUser(username: string): Promise<void> {
  if (!(await gatewayRunning())) return;
  // Hard-evict any live session first, then drop the account + files.
  await gwExec(["sh", "-c", `pkill -u ${shellQuote(username)} 2>/dev/null || true`]);
  await gwExec([
    "sh",
    "-c",
    `deluser ${shellQuote(username)} 2>/dev/null || true`,
  ]);
  await gwExec([
    "sh",
    "-c",
    `rm -rf /data/ssh-gateway/keys/${shellQuote(username)} ` +
      `/data/ssh-gateway/map/${shellQuote(username)}`,
  ]);
}

/**
 * Rebuild the gateway's account/key/map projection from the store (the sole
 * source of truth — ADR-0002). Called on gateway first boot and after drift.
 * The store always leads; a reconcile after any mutation is always correct.
 */
export async function reconcileGateway(): Promise<void> {
  if (!(await gatewayRunning())) return;
  const users = read().devSshUsers;
  for (const u of users) {
    await provisionUser(u).catch(() => {});
  }
}

/** Quote a value for safe interpolation into a `sh -c` string. */
function shellQuote(s: string): string {
  return `'${String(s).replace(/'/g, `'\\''`)}'`;
}
